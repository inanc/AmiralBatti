
public class Grid {

	private char deger;
	private boolean vuruldu = false;

	public Grid(char deger) {
		this.deger = deger;
	}

	public char getDeger() {
		return deger;
	}

	public void setDeger(char deger) {
		this.deger = deger;
	}

	public boolean isVuruldu() {
		return vuruldu;
	}

	public void setVuruldu(boolean vuruldu) {
		this.vuruldu = vuruldu;
	}

}
